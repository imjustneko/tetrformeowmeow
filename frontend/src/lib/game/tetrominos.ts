export class BagRandomizer {
  private bag: PieceType[] = [];
  private readonly pieces: PieceType[] = ['I', 'O', 'T', 'S', 'Z', 'J', 'L'];

  private refill(): void {
    this.bag = [...this.pieces];
    for (let i = this.bag.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [this.bag[i], this.bag[j]] = [this.bag[j], this.bag[i]];
    }
  }

  next(): PieceType {
    if (this.bag.length === 0) this.refill();
    return this.bag.pop()!;
  }

  fillQueue(count: number): PieceType[] {
    const result: PieceType[] = [];
    for (let i = 0; i < count; i++) result.push(this.next());
    return result;
  }
}

export function getPieceMatrix(type: PieceType, rotation: 0 | 1 | 2 | 3): CellValue[][] {
  return PIECE_MATRICES[type][rotation];
}

export function getSpawnPosition(type: PieceType): { x: number; y: number } {
  return { x: 3, y: type === 'I' ? -1 : -1 };
}
